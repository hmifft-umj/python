# Import library python
import numpy as np

# Membuat array 1D
array1 = np.array([1, 2, 3, 4, 5])
array2 = np.array([5, 4, 3, 2, 1])

# Operasi pada array
hasil_penjumlahan = array1 + array2
hasil_pengurangan = array1 - array2
hasil_perkalian = array1 * array2
hasil_pembagian = array1 / array2

# Menampilkan pada terminal
print("Hasil Penjumlahan:", hasil_penjumlahan)
print("Hasil Pengurangan:", hasil_pengurangan)
print("Hasil Perkalian:", hasil_perkalian)
print("Hasil Pembagian:", hasil_pembagian)