def main():
  # Deklarasi variable untuk pengkondisian
  umur = 18
    
  if umur >= 18:
    # Blok kode yang dijalankan jika kondisi benar (True)
    print(f"Umur saya diatas {umur} tahun")
  else:
    # Blok kode yang dijalankan jika kondisi salah (False)
    print(f"Umur saya dibawah {umur} tahun")
  
if __name__ == "__main__":
  main()