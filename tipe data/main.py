x = 3.14
print('Tipe data x adalah',type(x).__name__)
# Tipe data x adalah float

x = "Hello world"
print('Tipe data x adalah',type(x).__name__)
# Tipe data x adalah str

x = True
print('Tipe data x adalah',type(x).__name__)
# Tipe data x adalah bool

x = [1,2,3]
print('Tipe data x adalah',type(x).__name__)
# Tipe data x adalah list

x = ("a","b","c")
print('Tipe data x adalah',type(x).__name__)
# Tipe data x adalah tuple

x = {1,2,3}
print('Tipe data x adalah',type(x).__name__)
# Tipe data x adalah set

x = {"name": "jhon", "age": "20"}
