def main():
  # Deklarasi variable untuk pengkondisian 
  fruits = ["apple", "banana", "cherry"]
  # Dengan pengulangan for, 
  # kita dapat menjalankan serangkaian pernyataan, 
  # sekali untuk setiap item dalam sebuah list, tuple, set, dll.
  for x in fruits:
    print(x)

if __name__ == "__main__":
  main()    